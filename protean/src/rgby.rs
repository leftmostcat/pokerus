pub(crate) fn gen_1_type_id_to_standard(id: u8) -> u8 {
    match id {
        0x00..=0x05 => id,
        0x07..=0x08 => id - 1,
        0x14..=0x1a => id - 10,

        _ => panic!("Invalid gen 1 type ID {id}"),
    }
}

pub(crate) fn type_id_to_gen_1_idx(idx: u8) -> Option<u8> {
    let id = match idx {
        0..=5 => idx,
        6..=7 => idx + 1,
        10..=16 => idx + 10,

        _ => return None,
    };

    Some(id)
}

